#include<iostream>
using namespace std;
int main()
{
	int a = 10;
	int b = 5;
	int c;
	c = a + b;
	cout<<"The value of c : "<<c<<endl;
	c = a - b;
	cout<<"The value of c : "<<c<<endl;
	c = a * b;
	cout<<"The value of c : "<<c<<endl;
	c = a / b;
	cout<<"The value of c : "<<c<<endl;
	c = a % b;
	cout<<"The value of c : "<<c<<endl;
	c = ++a;
	cout<<"The value of c : "<<c<<endl;
	c = --a;
	cout<<"The value of c : "<<c<<endl;
	return 0; 

}
